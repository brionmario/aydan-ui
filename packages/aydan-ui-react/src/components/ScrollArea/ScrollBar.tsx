'use client';

import * as ScrollAreaPrimitive from '@radix-ui/react-scroll-area';
import {cn} from '@aydan-ui/utils';
import {ForwardRefExoticComponent, forwardRef, RefAttributes, ForwardedRef, ReactElement} from 'react';

export type ScrollBarProps = ScrollAreaPrimitive.ScrollAreaScrollbarProps;

const ScrollBar: ForwardRefExoticComponent<ScrollBarProps & RefAttributes<HTMLDivElement>> = forwardRef<
  HTMLDivElement,
  ScrollBarProps
>(
  (
    {className, orientation = 'vertical', ...props}: ScrollBarProps,
    ref: ForwardedRef<HTMLDivElement>,
  ): ReactElement => (
    <ScrollAreaPrimitive.ScrollAreaScrollbar
      ref={ref}
      orientation={orientation}
      className={cn(
        'flex touch-none select-none transition-colors',
        orientation === 'vertical' && 'h-full w-2.5 border-l border-l-transparent p-[1px]',
        orientation === 'horizontal' && 'h-2.5 flex-col border-t border-t-transparent p-[1px]',
        className,
      )}
      {...props}
    >
      <ScrollAreaPrimitive.ScrollAreaThumb className="relative flex-1 rounded-full bg-border" />
    </ScrollAreaPrimitive.ScrollAreaScrollbar>
  ),
);

ScrollBar.displayName = ScrollAreaPrimitive.ScrollAreaScrollbar.displayName;

export default ScrollBar;
